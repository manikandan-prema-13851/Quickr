__declspec(noinline) struct MimeValue* createDataNode(struct MimeValue* data) {
	struct MimeValue* newData = (struct MimeValue*)calloc(1, sizeof(struct MimeValue));
	size_t valLen = strlen(data->exttype);
	size_t mimevalLen = strlen(data->mimetype);
	if (newData) {
		newData->exttype = (char*)calloc(valLen + 1, sizeof(char));
		if (newData->exttype != NULL) {
			strcpy(newData->exttype, data->exttype);
		}
			
		newData->mimetype = (char*)calloc(mimevalLen + 1, sizeof(char));
		if (newData->mimetype != NULL) {
			strcpy(newData->mimetype, data->mimetype);
		}
		newData->next = NULL;
	}
	return newData;
}


__declspec(noinline) struct ExtNode* createNodeTrie(UINT8 key, struct MimeValue* vp) {

	struct MimeValue* mimevalue = (struct MimeValue*)vp;
	struct ExtNode* node = (struct ExtNode*)calloc(1, sizeof(struct ExtNode));
	if (node != NULL) {
		node->left = NULL;
		node->right = NULL;
		node->nextLevel = NULL;
		if (mimevalue != NULL) {
			node->MimeValue = createDataNode(mimevalue);
		}
		else {
			node->MimeValue = NULL;

		}
		node->key = key;
	}
	return node;
}


__declspec(noinline) struct ExtNode* rotate_left_left(struct ExtNode* node) {
	struct ExtNode* a = node;
	struct ExtNode* b = a->left;

	a->left = b->right;
	b->right = a;

	return(b);
}


__declspec(noinline) struct ExtNode* rotate_left_right(struct ExtNode* node) {
	struct ExtNode* a = node;
	struct ExtNode* b = a->left;
	struct ExtNode* c = b->right;

	a->left = c->right;
	b->right = c->left;
	c->left = b;
	c->right = a;

	return(c);
}


__declspec(noinline) struct ExtNode* rotate_right_left(struct ExtNode* node) {
	struct ExtNode* a = node;
	struct ExtNode* b = a->right;
	struct ExtNode* c = b->left;

	a->right = c->left;
	b->left = c->right;
	c->right = b;
	c->left = a;

	return(c);
}


__declspec(noinline) struct ExtNode* rotate_right_right(struct ExtNode* node) {
	struct ExtNode* a = node;
	struct ExtNode* b = a->right;

	a->right = b->left;
	b->left = a;

	return(b);
}


__declspec(noinline) int node_height(struct ExtNode* node) {
	int height_left = 0;

	int height_right = 0;

	if (node->left) height_left = node_height(node->left);
	if (node->right) height_right = node_height(node->right);

	return height_right > height_left ? ++height_right : ++height_left;
}


__declspec(noinline) int balance_factor(struct ExtNode* node) {
	int bf = 0;

	if (node->left) bf += node_height(node->left);
	if (node->right) bf -= node_height(node->right);

	return bf;
}


__declspec(noinline) struct ExtNode* balance_node(struct ExtNode* node) {
	struct ExtNode* newroot = NULL;

	if (node->left)
		node->left = balance_node(node->left);
	if (node->right)
		node->right = balance_node(node->right);

	int bf = balance_factor(node);

	if (bf >= 2) {

		if (balance_factor(node->left) <= -1)
			newroot = rotate_left_right(node);
		else
			newroot = rotate_left_left(node);

	}
	else if (bf <= -2) {

		if (balance_factor(node->right) >= 1)
			newroot = rotate_right_left(node);
		else
			newroot = rotate_right_right(node);

	}
	else {

		newroot = node;
	}

	return(newroot);
}


__declspec(noinline) struct ExtNode* avl_balance(struct ExtNode* tree) {

	struct ExtNode* newroot = NULL;

	newroot = balance_node(tree);

	if (newroot != tree) {
		tree = newroot;
	}

	return tree;
}


__declspec(noinline) struct ExtNode* findAvailable(struct ExtNode* node, UINT8 index) {
	if (node != NULL) {
		if (node != NULL && node->key == index) {
			return node;
		}
		else if (node != NULL && node->key > index) {
			if (node->left != NULL) {
				return findAvailable(node->left, index);
			}
		}
		else {
			if (node->right != NULL) {
				return findAvailable(node->right, index);
			}
		}
	}
	return NULL;
}


__declspec(noinline) struct ExtNode* avl_insert(struct ExtNode* tree, UINT8 index, struct MimeValue* MimeValue) {


	struct ExtNode* node = NULL;
	struct ExtNode* next = NULL;
	struct ExtNode* last = tree;

	node = createNodeTrie(index, MimeValue);

	while (tree != NULL) {
		if (tree->key < index) {
			if (tree->right == NULL) {
				tree->right = node;
				break;
			}
			tree = tree->right;
		}
		else if (tree->key > index) {
			if (tree->left == NULL) {
				tree->left = node;
				break;
			}
			tree = tree->left;
		}
	}

	return node;
}


__declspec(noinline) void insert_extNode(struct ExtNode* node, struct signatureMap map) {
	int level = 0;
	struct MimeValue* keyValue = NULL;
	for (; level < map.key.size; level++) {
		if (level == (map.key.size - 1)) {
			keyValue = &map.value;
		}
		UINT8 index = (UINT8)map.key.arr[level];
		if (node->nextLevel == NULL) {
			node->nextLevel = createNodeTrie(index, keyValue);
			node = node->nextLevel;
		}
		else {
			struct ExtNode* n = findAvailable(node->nextLevel, index);
			if (n == NULL) {
				n = avl_insert(node->nextLevel, index, keyValue);
				node->nextLevel = avl_balance(node->nextLevel);
				node = n;
			}
			else {
				node = n;
				if (node->MimeValue != NULL && keyValue != NULL) {
					struct MimeValue* newdata = createDataNode(keyValue);
					struct MimeValue* currentData = node->MimeValue;
					while (currentData->next != NULL) {
						currentData = currentData = currentData->next;
					}
					currentData->next = newdata;
				}
				if (keyValue != NULL && node->MimeValue == NULL) {
					node->MimeValue = createDataNode(keyValue);
				}
			}
		}
	}
}


